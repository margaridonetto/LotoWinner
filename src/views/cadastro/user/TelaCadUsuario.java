    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Usuario user = new Usuario();
        PrepState pst = new PrepState();
        user.setNome(jTextFieldNome.getText());
        user.setCpf(jFormattedTextFieldCpf.getText());
        user.setTelefone(jFormattedTextFieldTelefone.getText());
        user.setCelular(jFormattedTextFieldCelular.getText());
        user.setEmail(jTextFieldEmail.getText());
        user.setUser(jTextFieldUsuario.getText());
        user.setSenha(jPasswordFieldSenha.getText());
        pst.salvar(user);
        dispose(); //fecha janela atual
    }//GEN-LAST:event_jButtonSalvarActionPerformed

